package org.qubership.atp.macros.core.exception;

import org.antlr.v4.runtime.BaseErrorListener;
import org.antlr.v4.runtime.RecognitionException;
import org.antlr.v4.runtime.Recognizer;
import org.antlr.v4.runtime.misc.ParseCancellationException;

import lombok.extern.slf4j.Slf4j;

@Slf4j
public class ThrowingErrorListener extends BaseErrorListener {

    public static final ThrowingErrorListener ERROR_LISTENER = new ThrowingErrorListener();

    @Override
    public void syntaxError(Recognizer<?, ?> recognizer, Object offendingSymbol, int line, int charPositionInLine,
                            String msg, RecognitionException e) {
        log.error(msg, new ParseCancellationException("line " + line + ":" + charPositionInLine));
    }
}
